var numero=30;

switch (numero) {
    case 1:
        console.log("¡soy un Uno!")
        break;                              // es obligatorio agregar BREAK
    case 10:
        console.log("¡soy un Diez!")
        break;
    case 30:
        console.log("¡soy un Treinta!")
        break;
    default:
        console.log("No soy nada")
    
}